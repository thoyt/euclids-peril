function _init()
 mode = "splash"
 circles = {}
 grid = {}

 n = 7 -- tile grid dims
 m = 7
 mtop = 14 -- margins in pixels
 mleft = 8

 level = 1
 start_life=20
 hero = {x=0,y=0,spr=6,life=start_life,maxlife=30}
 lifebar = {x=58,y=3,h=5,col=8}
 heart = {x=0,y=0,placed=false,val=5,spr=51}
 p_heart = 0.35

 items = {}
 inventory_x = mleft
 inventory_y = 2 -- inventory ypos
 items[1] = {id=1,spr=38,name="pickax",own=true,func=break_wall,x=0,y=0,placed=false,level=-1}
 items[2] = {id=2,spr=39,name="point",own=false,func=point_attack,x=0,y=0,placed=false,level=4}
 items[3] = {id=3,spr=40,name="line",own=false,func=line_attack,x=0,y=0,placed=false,level=8}
 items[4] = {id=4,spr=41,name="triangle",own=false,func=triangle_attack,x=0,y=0,placed=false,level=12}
 items[5] = {id=5,spr=42,name="square",own=false,func=square_attack,x=0,y=0,placed=false,level=20}
 selected = 1

 goal = {x=0,y=0,spr=8}

 sphere_types = {}
 sphere_types[0] = {spr=64,frozen_spr=70,name="red"}
 sphere_types[1] = {spr=66,frozen_spr=72,name="green"}
 sphere_types[2] = {spr=68,frozen_spr=77,name="blue"}
 spheres = {}

 t = 0
 w = 16 -- the width of our tiles

 tiles = {}
 tiles[0] = {spr=0,name="blue"}
 tiles[1] = {spr=2,name="red"}
 tiles[2] = {spr=4,name="green"}
 tiles[3] = {spr=32,name="magic"}
 tiles[4] = {spr=36,name="wall"}
 ntiles = 4
 hearts_in_walls_prob = 0
 tut_count = 0

 init_new_game()
 init_splash()
end

function init_tutorial_grid()
 for i=0,n-1 do
  grid[i] = {}
  for j=0,m-1 do
   grid[i][j] = {}
   if j%2 == 0 then
    grid[i][j] = 4
   else
    grid[i][j] = 0
   end
  end
 end
 goal.x = m-1
 goal.y = 1
 hero.x = n-1
 hero.y = m-2
end

function init_new_game()
 level = 1
 hero.life = start_life
 for item in all(items) do
  if item.name != "pickax" then
   item.own=false
  end
 end
 selected = 1
 hero.x = 0
 hero.y = 0
 game_over_pal = 15
end

-- utilities --

function grid_at(pos)
 return grid[pos.x][pos.y]
end

function tile_at(pos)
 return tiles[grid_at(pos)]
end

function valid_pos(pos)
 return not (
   pos.x >= n or
   pos.y >= m or
   pos.x < 0 or
   pos.y < 0)
end

function sametile(u,v)
 return u.x==v.x and u.y==v.y
end

-- game gen --

function compute_grid()
 for i=0,n-1 do
  grid[i] = {}
  for j=0,m-1 do
   grid[i][j] = {}
   if is_occupied(i,j) then
    grid[i][j] = flr(rnd(ntiles))
   else
    grid[i][j] = flr(rnd(ntiles+1))
   end
  end
 end
 local old_goal = {x=goal.x, y=goal.y}
 while (sametile(old_goal,goal)) do
  goal.x = flr(rnd(n))
  goal.y = flr(rnd(m))
 end
 -- goal not on wall
 grid[goal.x][goal.y] = flr(rnd(ntiles))
end

function get_sphere_type()
 return sphere_types[flr(rnd(#sphere_types+1))]
end

function gen_sphere()
 local sphere = {}
 local sphere_type = get_sphere_type()
 sphere.x = hero.x
 sphere.y = hero.y
 while (sametile(sphere,hero)) do
  sphere.x = flr(rnd(n))
  sphere.y = flr(rnd(m))
 end
 sphere.spr = sphere_type.spr
 sphere.frozen_spr = sphere_type.frozen_spr
 sphere.name = sphere_type.name
 sphere.frozen_turns = 0
 sphere.health = flr(rnd(5)) + 1
 return sphere
end

function place_enemies()
  -- number/type of enemies can depend on level
 spheres = {}
 local n_spheres = level / 8 + 1
 for i=0,n_spheres do
  add(spheres, gen_sphere())
 end
end

function place_heart()
 heart.x = hero.x
 heart.y = hero.y
 if (rnd(1) < p_heart) then
  while(sametile(hero,heart)) do
   heart.x = flr(rnd(n))
   heart.y = flr(rnd(m))
  end
  heart.placed = true
  hearts_in_walls_prob = 0
 else
  heart.placed = false
  hearts_in_walls_prob = 0.25
 end
end

function place_item(item)
 item.x = hero.x
 item.y = hero.y
 if level == item.level then
  while (sametile(hero, item)) do
   item.x = flr(rnd(n))
   item.y = flr(rnd(m))
  end
  item.placed = true
 else
  item.placed = false
 end
end

function place_powerups()
 place_heart()
 foreach(items, place_item)
end

function draw_powerup(powerup)
 if powerup.placed then
  spr(powerup.spr, mleft+powerup.x*w+8,mtop+powerup.y*w+8,1,1)
 end
end

function draw_powerups()
 draw_powerup(heart)
 foreach(items, draw_powerup)
end

function next_level()
 if mode == "main" then
  place_enemies()
  place_powerups()
  compute_grid()
 elseif mode == "tutorial_main" then
  tut_count += 1
  init_new_game()
  init_tutorial_grid()
  place_enemies()
  spheres[1].health = 1
  spheres[2].health = 1
  if tut_count == 2 then
   items[3].x=n-3
   items[3].y=m-2
   items[3].placed = true
   spheres[1].x=n-8
   spheres[1].y=m-4
   spheres[2].x=n-1
   spheres[2].y=m-4
  spheres[1].health = 2
  spheres[2].health = 2
  end
  if tut_count == 3 then
   mode = "splash"
  end
 end
end

-- input processing and movement

function toggle_item()
 selected += 1
 while (selected <= #items) and
       (not items[selected].own) do
  selected += 1
 end
 if selected > #items then selected = 1 end
end

function get_sphere_at(pos)
 for sphere in all(spheres) do
  if sametile(sphere, pos) then
   return sphere
  end
 end
end

function freeze_sphere(sphere)
  sphere.health -= 1
  if items[selected].name!='pickax' then
   sphere.health -= 1
  end
end

function freeze_sphere_at(x,y)
 if is_occupied(x,y) then
  local s = get_sphere_at({x=x,y=y})
  freeze_sphere(s)
 end
end

function hero_attack_sphere(sphere)
 items[selected].func(sphere)
 freeze_sphere(sphere)
 sfx(5)
end

function point_attack(sphere)
 circfill(mleft+sphere.x*w+8,mtop+sphere.y*w+8,5,8)
end

function line_attack(sphere)
 dx = hero.x-sphere.x
 dy = hero.y-sphere.y
 tx = hero.x+dx
 ty = hero.y+dy
 draw_line(sphere.x,sphere.y,tx,ty,8)
 freeze_sphere_at(tx,ty)
end

function triangle_attack(sphere)
 dx = hero.x - sphere.x
 dy = hero.y - sphere.y
 t1x = hero.x+dy
 t1y = hero.y+dx
 t2x = hero.x-dy
 t2y = hero.y-dx
 draw_triangle(sphere.x,sphere.y,t1x,t1y,t2x,t2y,8)
 -- check other tiles and freeze spheres there
 freeze_sphere_at(t1x,t1y)
 freeze_sphere_at(t2x,t2y)
end

function square_attack(sphere)
 dx = hero.x-sphere.x
 dy = hero.y-sphere.y
 t1x = hero.x+dy
 t1y = hero.y+dx
 t2x = hero.x+dx
 t2y = hero.y+dy
 t3x = hero.x-dy
 t3y = hero.y-dx
 draw_square(sphere.x,sphere.y,t1x,t1y,t2x,t2y,t3x,t3y,8)
 freeze_sphere_at(t1x,t1y)
 freeze_sphere_at(t2x,t2y)
 freeze_sphere_at(t3x,t3y)
end

function draw_line(x1,y1,x2,y2,col)
 line(mleft+8+x1*w,mtop+8+y1*w,mleft+8+x2*w,mtop+8+y2*w,col)
end

function draw_triangle(x1,y1,x2,y2,x3,y3,col)
 draw_line(x1,y1,x2,y2,col)
 draw_line(x2,y2,x3,y3,col)
 draw_line(x3,y3,x1,y1,col)
end

function draw_square(x1,y1,x2,y2,x3,y3,x4,y4,col)
 draw_line(x1,y1,x2,y2,col)
 draw_line(x2,y2,x3,y3,col)
 draw_line(x3,y3,x4,y4,col)
 draw_line(x4,y4,x1,y1,col)
end

function player_turn()
 if btnp(0) or btnp(1) or btnp(2) or btnp(3) then
  new_pos = {}
  new_pos.x = hero.x
  new_pos.y = hero.y
  if btnp(0) then
   new_pos.x -= 1
  elseif btnp(1) then
   new_pos.x += 1
  elseif btnp(2) then
   new_pos.y -= 1
  elseif btnp(3) then
   new_pos.y += 1
  end

  if is_occupied(new_pos.x, new_pos.y) then
   hero_attack_sphere(get_sphere_at(new_pos))
  elseif allowed(new_pos) then
   hero.x = new_pos.x
   hero.y = new_pos.y
  elseif valid_pos(new_pos) then
   items[selected].func(new_pos)
  end
  check_tile()
 elseif btnp(4) then
  toggle_item()
 end
end

function remove_dead_spheres()
 for sphere in all(spheres) do
  if (sphere.health <= 0) then
   del(spheres, sphere)
  end
 end
end

function npc_turn()
 -- move the spheres and attack hero if in range
 remove_dead_spheres()
 move_spheres_or_attack_hero()
end

function check_for_game_over()
  if hero.life <= 0 then
   sfx(7)
   mode = "game over"
 end
end

function process_input()
 if btnp(0) or btnp(1) or btnp(2) or btnp(3) or btnp(4) then
  new_level = false
  player_turn()
  if not new_level then
   npc_turn()
   check_for_game_over()
  end
 end
end

function allowed(pos)
 if valid_pos(pos) then
  tile = tile_at(pos)
  return tile.name != 'wall'
 else
  return false
 end
end

function is_occupied(x,y)
 if x == hero.x and y == hero.y then return true end
 return is_occupied_by_sphere(x,y)
end

function is_occupied_by_sphere(x,y)
 for sphere in all(spheres) do
  if x == sphere.x and y == sphere.y then return true end
 end
 return false
end

function break_wall(pos)
 if tile_at(pos).name == "wall" then
  sfx(3)
  grid[pos.x][pos.y] = flr(rnd(ntiles))
  if rnd() < hearts_in_walls_prob then
   hero.life = min(30, hero.life + 5)
   sfx(6)
   hearts_in_walls_prob = 0
  end
 end
end

function swing(pos)
 if tile_at(pos).name == "wall" then
  sfx(4)
 end
end

function check_tile()
 if sametile(hero,heart) and heart.placed then
  hero.life = min(30, hero.life + heart.val)
  sfx(6)
  heart.placed = false
 end
 for item in all(items) do
  if sametile(hero,item) and item.placed then
   item.own = true
   item.placed = false
   sfx(6)
  end
 end
 if sametile(hero,goal) then
  sfx(1)
  level+=1
  new_level = true
  next_level()
 end
end

function move_sphere_toward_hero(sphere)
  local dx
  local dy
  local new_pos1 = {}
  local new_pos2 = {}
  new_pos1.x = sphere.x
  new_pos1.y = sphere.y
  new_pos2.x = sphere.x
  new_pos2.y = sphere.y
  dx = (hero.x - sphere.x)
  dy = (hero.y - sphere.y)
  new_pos1.x += sgn(dx)
  new_pos2.y += sgn(dy)
  if (abs(dx) >= abs(dy) and allowed(new_pos1) and
   not is_occupied(new_pos1.x, new_pos1.y)) then
   sphere.x = new_pos1.x
   sphere.y = new_pos1.y
  elseif allowed(new_pos2) and
   not is_occupied(new_pos2.x, new_pos2.y) then
   sphere.x = new_pos2.x
   sphere.y = new_pos2.y
  end
end

function within_attack_range(sphere)
  dx = (hero.x - sphere.x)
  dy = (hero.y - sphere.y)
  return (abs(dx) == 1 and abs(dy) == 0) or
         (abs(dy) == 1 and abs(dx) == 0)
end

function attack(sphere, p)
 hero.life -= p
 if hero.life > 0 then
  rectfill(0,0,128,128,8)
 end
end

function hero_protected(sphere)
 local t = tile_at(hero)
 return t.name=='magic' or (sphere.name==t.name)
end

function sphere_turn_or_attack_hero(sphere)
 -- attack hero if within range, otherwise move toward hero if possible
 if sphere.frozen_turns == 0 then
  if within_attack_range(sphere) then
   if hero_protected(sphere) then
    sfx(4)
    attack(sphere, flr(sphere.health/2))
   else
    attack(sphere,sphere.health)
   end
  else
   move_sphere_toward_hero(sphere)
  end
 else
  sphere.frozen_turns -= 1
 end
end

function move_spheres_or_attack_hero()
 foreach(spheres, sphere_turn_or_attack_hero)
end

-- drawing functions

function draw_bg()
 map(0,0,0,0,16,16)
end

function draw_inventory()
 function display_item(i)
  if i.own then
   spr(i.spr,inventory_x+i.id*8,inventory_y,1,1)
  end
 end
 for i=1,#items do -- draw inventory background
  spr(50,inventory_x+items[i].id*8,inventory_y,1,1)
 end
 foreach(items, display_item)
 spr(35,inventory_x+items[selected].id*8,inventory_y,1,1)
end

function draw_lifebar()
 local col = 8
 for i=0,hero.maxlife do
  if i >= hero.life then col = 5 end
  line(lifebar.x+i*2,
   lifebar.y,
   lifebar.x+i*2,
   lifebar.y+lifebar.h,
   col)
 end
end

function draw_grid()
 for i=0,n-1 do
  for j=0,m-1 do
   spr(tiles[grid[i][j]].spr,
    mleft+i*w,mtop+j*w,2,2)
  end
 end
 spr(goal.spr,mleft+goal.x*w,
  mtop+goal.y*w,2,2)
end

function draw_enemies()
 for sphere in all(spheres) do
  x = mleft+sphere.x*w
  y = mtop+sphere.y*w
  if sphere.frozen_turns > 0 then
   spr(sphere.frozen_spr,x,y,2,2)
  else
   spr(sphere.spr,x,y,2,2)
  end
  print(sphere.health,x,y+10,7)
 end
end

function init_splash()
 local ncircles = 50
 music(1)
 for i=0,ncircles do
  local circle = {}
  circle.x = flr(rnd(148))
  circle.y = flr(rnd(148))
  circle.omega = rnd(2)
  circle.r = rnd(7) + 1
  circle.amp = rnd(2)
  circle.col = 8 + 2*flr(rnd(3)) --rgb colorscheme
  add(circles, circle)
 end
end

function update_circles()
 for circle in all(circles) do
  circle.x += circle.amp * sin((circle.omega * (1+t) / 400))
  circle.y += circle.amp * cos((circle.omega * (1+t) / 100))
 end
end

function draw_circles()
 local p = sin(t/300)
 local r = 40 + 32*p*p
 for circle in all(circles) do
  circfill(circle.x,circle.y,circle.r,circle.col)
 end
 circfill(64,64,r,0) --clipping circle
end

function splash_screen()
 update_circles()
 draw_circles()
 local euclid_spr = 96
 local euclid_pos = {x=12,y=40}
 local peril_spr = 144
 spr(euclid_spr, euclid_pos.x, euclid_pos.y, 13, 3)
 sspr(95,40,8,8,euclid_pos.x+95, euclid_pos.y-8) -- don't ask
 spr(peril_spr, 26, 70, 10, 2)
 pal()
 print("'s",112,43,7) -- replace with pixels ^___^
 print("z: start",45,100,7)
 print("x: tutorial",40,110,7)
 if btnp(4) then
  mode = "main"
  music(-1)
  init_new_game()
  next_level()
 elseif btnp(5) then
  mode = "tutorial"
  music(-1)
  tut_count=0
 end
end

function tutorial_screen()
 init_new_game()
 init_tutorial_grid()
 mode="tutorial_main"
end

function game_over()
 if t%2 == 0 or game_over_pal==0 then
  if game_over_pal > 0 then
   pal(game_over_pal, 0, 1)
   game_over_pal -= 1
   tt = 0
  else
   cls()
   pal()
   herox = mleft+hero.x*w
   heroy = mtop+hero.y*w
   spr(hero.spr,herox,heroy,2,2)
   tt += 1
   print("game over :<", 40, 40, 6)
   print("you got to level "..level..". nice.",17,50,6)
   if tt > 50 then
    print("press anything for title",15,100,9)
    if btnp(0) or btnp(1) or btnp(2) or btnp(3) or btnp(4) or btnp(5) then
     _init()
    end
   end
  end
 end
end

function draw_tut_messages()
 if mode=="tutorial_main" then
  if tut_count == 0 then
   print("bump against walls with", mleft, mtop+6*w, 7)
   print("pickaxe to break them", mleft, mtop+(6.5*w), 7)
  end
  if tut_count == 1 then
   print("bump against enemies", mleft, mtop+6*w, 7)
   print("to freeze/kill them", mleft, mtop+(6.5*w), 7)
  end
  if tut_count == 2 then
   print("pick up new weapons", mleft, mtop+6*w, 7)
   print("use z to switch wpn", mleft, mtop+(6.5*w), 7)
  end
 end
end

function _update()
 t+=1
end

function _draw()
 if mode=="splash" then
  cls()
  splash_screen()
 elseif mode=="tutorial" then
  tutorial_screen()
 elseif mode=="game over" then
  game_over()
 elseif mode=="main" or mode=="tutorial_main" then
  cls()
  draw_bg()
  print(level,2,3,7)
  draw_inventory()
  draw_lifebar()
  draw_grid()
  process_input()
  spr(hero.spr,mleft+hero.x*w,mtop+hero.y*w,2,2)
  draw_powerups()
  draw_enemies()
  draw_tut_messages()
 end
end

