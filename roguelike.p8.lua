__lua__
function _init()
 grid = {}
 par = {} -- params we use only sometimes
 par.n = 7 -- tile grid dims
 par.m = 7
 mtop = 8 -- margins in pixels
 mleft = 8

 hero = {}
 hero.x = 0
 hero.y = 0
 hero.spr = 6 -- sprite location

 t = 0
 w = 16 -- the width of our tiles

 tiles = {}
 tiles[0] = {spr=0,name="blue"}
 tiles[1] = {spr=2,name="red"}
 tiles[2] = {spr=32,name="magic"}
 compute_grid()
 cls()
 draw_bg()
end

function move_hero()
 if btnp(0) and hero.x > 0 then
  hero.x -= 1
 end
 if btnp(1) and hero.x < par.n then
  hero.x += 1
 end
 if btnp(2) and hero.y > 0 then
  hero.y -= 1
 end
 if btnp(3) and hero.y < par.m then
  hero.y += 1
 end
 if btnp(0) or btnp(1) or btnp(2) or btnp(3) then check_tile() end
end

function compute_grid()
 for i=0,par.n-1 do
  grid[i] = {}
  for j=0,par.m-1 do
   grid[i][j] = {}
   grid[i][j] = flr(rnd(3))
  end
 end
end

function draw_grid()
 for i=0,par.n-1 do
  for j=0,par.m-1 do
   spr(tiles[grid[i][j]].spr,
    mleft+i*w,mtop+j*w,2,2)
  end
 end
end

function check_tile()
 if tiles[grid[hero.x][hero.y]].name=='magic' then
  sfx(0)
 end
end

function draw_bg()
 for i=0,128 do
  for j=0,128 do
    pset(i,j,(i+j)%2)
  end
 end
end

function _update()
 t+=1
end

function _draw()
 draw_grid()
 move_hero()
 spr(hero.spr,
  mleft+hero.x*w,mtop+hero.y*w,2,2)
end

