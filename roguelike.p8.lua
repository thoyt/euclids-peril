__lua__
function break_wall()
 sfx(2)
end

function _init()
 grid = {}
 par = {} -- params we use only sometimes
 par.n = 7 -- tile grid dims
 par.m = 7
 mtop = 14 -- margins in pixels
 mleft = 8

 hero = {}
 hero.x = 0
 hero.y = 0
 hero.spr = 6 -- sprite location

 items = {}
 items[1] = {spr=38,name="pickax",own=true,func=break_wall,selected=true,x=16}
 items[2] = {spr=39,name="sword",own=true,func=break_wall,selected=false,x=24}
 items[3] = {spr=40,name="foo",own=true,func=break_wall,selected=false,x=32}

 goal = {}
 goal.x = 0
 goal.y = 0
 goal.spr = 8

 t = 0
 w = 16 -- the width of our tiles

 tiles = {}
 tiles[0] = {spr=0,name="blue"}
 tiles[1] = {spr=2,name="red"}
 tiles[2] = {spr=32,name="magic"}
 tiles[3] = {spr=36,name="wall"}
 compute_grid()
end

function move_hero()
 new_pos = {}
 new_pos.x = hero.x
 new_pos.y = hero.y
 if btnp(0) then
  new_pos.x -= 1
 elseif btnp(1) then
  new_pos.x += 1
 elseif btnp(2) then
  new_pos.y -= 1
 elseif btnp(3) then
  new_pos.y += 1
 end

 if allowed(new_pos) then
  hero.x = new_pos.x
  hero.y = new_pos.y
  if btnp(0) or btnp(1) or btnp(2) or btnp(3) then check_tile() end
 else
  use_item(new_pos)
  sfx(2)
 end
end

function get_item()
 function check_item(i)
  if i.selected then
    my_item = i
  end
 end
 foreach(items, check_item)
end

function use_item(pos)

end

function grid_at(pos)
 return grid[new_pos.x][new_pos.y]
end

function tile_at(pos)
 return tiles[grid_at(pos)]
end

function allowed(pos)
 if pos.x >= par.n or
    pos.y >= par.m or
    pos.x < 0 or
    pos.y < 0 then
  return false
 end
 tile = tile_at(pos)
 return tile.name != 'wall'
end

function compute_grid()
 for i=0,par.n-1 do
  grid[i] = {}
  for j=0,par.m-1 do
   grid[i][j] = {}
   if i == hero.x and
      j == hero.y then
   	grid[i][j] = flr(rnd(3))
   else
    grid[i][j] = flr(rnd(4))
   end
  end
 end
 goal.x = flr(rnd(par.n))
 goal.y = flr(rnd(par.m))
 -- goal not on wall
 grid[goal.x][goal.y] = flr(rnd(3))
end

function draw_inventory()
 function display_item(i)
  if i.own then
   spr(i.spr,i.x,4,1,1)
  end
  if i.selected then
   spr(35,i.x,4,1,1)
  end
 end
 foreach(items, display_item)
end

function draw_grid()
 for i=0,par.n-1 do
  for j=0,par.m-1 do
   spr(tiles[grid[i][j]].spr,
    mleft+i*w,mtop+j*w,2,2)
  end
 end
 spr(goal.spr,mleft+goal.x*w,
  mtop+goal.y*w,2,2)
end

function sametile(u,v)
 return u.x==v.x and u.y==v.y
end

function check_tile()
 if tile_at(hero).name=='magic' then
  sfx(0)
 end
 if sametile(hero,goal) then
  sfx(1)
  compute_grid()
 end
end

function draw_bg()
 map(0,0,0,0,16,16)
end

function _update()
 t+=1
end

function _draw()
 cls()
 draw_bg()
 draw_inventory()
 draw_grid()
 move_hero()
 spr(hero.spr,
  mleft+hero.x*w,mtop+hero.y*w,2,2)
end

