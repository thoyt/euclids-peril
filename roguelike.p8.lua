__lua__
function _init()
 grid = {}

 n = 7 -- tile grid dims
 m = 7
 mtop = 14 -- margins in pixels
 mleft = 8

 level = 1
 hero = {x=0,y=0,spr=6,life=20,maxlife=30}
 lifebar = {x=58,y=2,h=5,col=8}

 items = {}
 items[1] = {spr=38,name="pickax",own=true,func=break_wall,x=mleft+1}
 items[2] = {spr=39,name="sword",own=false,func=swing,x=mleft+9}
 items[3] = {spr=40,name="foo",own=false,func=swing,x=mleft+17}
 selected = 1
 inventory_y = 2 -- inventory ypos

 goal = {x=0,y=0,spr=8}

 spheres = {}

 t = 0
 w = 16 -- the width of our tiles

 tiles = {}
 tiles[0] = {spr=0,name="blue"}
 tiles[1] = {spr=2,name="red"}
 tiles[2] = {spr=4,name="green"}
 tiles[3] = {spr=32,name="magic"}
 tiles[4] = {spr=36,name="wall"}
 ntiles = 4
 next_level()
end

-- utilities --

function grid_at(pos)
 return grid[pos.x][pos.y]
end

function tile_at(pos)
 return tiles[grid_at(pos)]
end

function valid_pos(pos)
 return not (
   pos.x >= n or
   pos.y >= m or
   pos.x < 0 or
   pos.y < 0)
end

function sametile(u,v)
 return u.x==v.x and u.y==v.y
end


-- game gen --

function compute_grid()
 for i=0,n-1 do
  grid[i] = {}
  for j=0,m-1 do
   grid[i][j] = {}
   if is_occupied(i,j) then
    grid[i][j] = flr(rnd(ntiles))
   else
    grid[i][j] = flr(rnd(ntiles+1))
   end
  end
 end
 goal.x = flr(rnd(n))
 goal.y = flr(rnd(m))
 -- goal not on wall
 grid[goal.x][goal.y] = flr(rnd(ntiles))
end

function gen_sphere()
 local sphere = {}
 sphere.x = hero.x
 sphere.y = hero.y
 while (sametile(sphere,hero)) do
  sphere.x = flr(rnd(n))
  sphere.y = flr(rnd(m))
 end
 sphere.spr = 64
 return sphere
end

function place_enemies()
  -- number/type of enemies can depend on level
 spheres = {}
 local n_spheres = level / 8 + 1
 for i=0,n_spheres do
  add(spheres, gen_sphere())
 end
end

function next_level()
 place_enemies()
 compute_grid()
end

-- input processing and movement

function toggle_item()
 selected += 1
 if selected > #items then selected = 1 end
end

function process_input()
 if btnp(0) or btnp(1) or btnp(2) or btnp(3) then

  new_pos = {}
  new_pos.x = hero.x
  new_pos.y = hero.y
  if btnp(0) then
   new_pos.x -= 1
  elseif btnp(1) then
   new_pos.x += 1
  elseif btnp(2) then
   new_pos.y -= 1
  elseif btnp(3) then
   new_pos.y += 1
  end

  if is_occupied(new_pos.x, new_pos.y) then
    sfx(2) -- fiiight
  elseif allowed(new_pos) then
   hero.x = new_pos.x
   hero.y = new_pos.y
  elseif valid_pos(new_pos) then
   items[selected].func(new_pos)
  end

  turn()
 elseif btnp(4) then
  toggle_item()
 end

end

function turn()
 check_tile()
 move_spheres()
end

function allowed(pos)
 if valid_pos(pos) then
  tile = tile_at(pos)
  return tile.name != 'wall'
 else
  return false
 end
end

function is_occupied(x,y)
 if x == hero.x and y == hero.y then return true end
 for sphere in all(spheres) do
  if x == sphere.x and y == sphere.y then return true end
 end
 return false
end

function break_wall(pos)
 if tile_at(pos).name == "wall" then
  sfx(3)
  grid[pos.x][pos.y] = flr(rnd(ntiles))
 end
end

function swing(pos)
 if tile_at(pos).name == "wall" then
  sfx(4)
 end
end

function check_tile()
 if tile_at(hero).name=='magic' then
  sfx(0)
 end
 if sametile(hero,goal) then
  sfx(1)
  level+=1
  next_level()
 end
end

function sphere_turn(sphere)
 -- try to move toward hero, otherwise chill
 local dx
 local dy
 local new_pos = {}
 new_pos.x = sphere.x
 new_pos.y = sphere.y
 dx = (hero.x - sphere.x)
 dy = (hero.y - sphere.y)
 if abs(dx) > abs(dy) then
  new_pos.x += sgn(dx)
 else
  new_pos.y += sgn(dy)
 end
 if allowed(new_pos) and
 not is_occupied(new_pos.x, new_pos.y) then
  sphere.x = new_pos.x
  sphere.y = new_pos.y
 end
end

function move_spheres()
 foreach(spheres, sphere_turn)
end

-- drawing functions

function draw_bg()
 map(0,0,0,0,16,16)
end

function draw_inventory()
 function display_item(i)
  if i.own then
   spr(i.spr,i.x,inventory_y,1,1)
  end
 end
 for i=1,#items do -- draw inventory background
  spr(50,items[i].x,inventory_y,1,1)
 end
 foreach(items, display_item)
 spr(35,items[selected].x,inventory_y,1,1)
end

function draw_lifebar()
 local col = 8
 for i=0,hero.maxlife do
  if i > hero.life then col = 5 end
  line(lifebar.x+i*2,
   lifebar.y,
   lifebar.x+i*2,
   lifebar.y+lifebar.h,
   col)
 end
end

function draw_grid()
 for i=0,n-1 do
  for j=0,m-1 do
   spr(tiles[grid[i][j]].spr,
    mleft+i*w,mtop+j*w,2,2)
  end
 end
 spr(goal.spr,mleft+goal.x*w,
  mtop+goal.y*w,2,2)
end

function draw_enemies()
 for sphere in all(spheres) do
  spr(sphere.spr,mleft+sphere.x*w,mtop+sphere.y*w,2,2)
 end
end

function _update()
 t+=1
end

function _draw()
 cls()
 draw_bg()
 print(level,2,2,7)
 draw_inventory()
 draw_lifebar()
 draw_grid()
 process_input()

 spr(hero.spr,mleft+hero.x*w,mtop+hero.y*w,2,2)
 draw_enemies()
end

